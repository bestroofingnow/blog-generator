// Helper function to get all checked services for an industry
export function getDefaultServices(industryKey: string): string[] {
  const industry = INDUSTRIES[industryKey];
  if (!industry) return [];

  const services: string[] = [];

  Object.values(industry.services).forEach((category) => {
    category.forEach((service) => {
      if (service.checked) {
        services.push(service.value);
      }
    });
  });

  return services;
}

// Helper function to get all checked USPs for an industry
export function getDefaultUSPs(industryKey: string): string[] {
  const industry = INDUSTRIES[industryKey];
  if (!industry) return [];

  return industry.usps
    .filter((usp) => usp.checked)
    .map((usp) => usp.value);
}

// Get industry options for dropdown
export function getIndustryOptions(): { value: string; label: string; icon: string }[] {
  return Object.entries(INDUSTRIES).map(([key, config]) => ({
    value: key,
    label: config.name,
    icon: config.icon,
  }));
}

// Get the effective industry name (uses custom name if industry is "custom" and custom name is provided)
export function getEffectiveIndustryName(industryKey: string, customIndustryName?: string): string {
  if (industryKey === "custom" && customIndustryName) {
    return customIndustryName;
  }
  const industry = INDUSTRIES[industryKey];
  return industry ? industry.name : industryKey;
}

// Get industry config with custom name override
export function getIndustryConfig(industryKey: string, customIndustryName?: string): IndustryConfig | null {
  const industry = INDUSTRIES[industryKey];
  if (!industry) return null;

  // If it's a custom industry with a custom name, override some properties
  if (industryKey === "custom" && customIndustryName) {
    return {
      ...industry,
      name: customIndustryName,
      serviceNoun: customIndustryName.toLowerCase(),
      servicePlural: `${customIndustryName.toLowerCase()} services`,
      providerNoun: `${customIndustryName.toLowerCase()} professionals`,
      description: `Professional ${customIndustryName.toLowerCase()} services.`,
    };
  }

  return industry;
}
